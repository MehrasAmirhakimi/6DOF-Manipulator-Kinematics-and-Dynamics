function transfershow(TransferMatrix)
R=TransferMatrix(1:3,1:3);
P=TransferMatrix(1:3,4);
o=[0 P(1,1);0 P(2,1);0 P(3,1)];
x=[1 R(1,1)+P(1,1);0 R(2,1)+P(2,1);0 R(3,1)+P(3,1)];
y=[0 R(1,2)+P(1,1);1 R(2,2)+P(2,1);0 R(3,2)+P(3,1)];
z=[0 R(1,3)+P(1,1);0 R(2,3)+P(2,1);1 R(3,3)+P(3,1)];
figure;
hold on;
grid on;
axis([-10 10 -10 10 -10 10]);
plot3([o(1,1),x(1,1)],[o(2,1),x(2,1)],[o(3,1),x(3,1)],'color','m');
plot3([o(1,1),y(1,1)],[o(2,1),y(2,1)],[o(3,1),y(3,1)],'color','c');
plot3([o(1,1),z(1,1)],[o(2,1),z(2,1)],[o(3,1),z(3,1)],'color','b');
plot3([o(1,1),o(1,2)],[o(2,1),o(2,2)],[o(3,1),o(3,2)],'color','k','LineStyle',':');
plot3([o(1,2),x(1,2)],[o(2,2),x(2,2)],[o(3,2),x(3,2)],'color','m');
plot3([o(1,2),y(1,2)],[o(2,2),y(2,2)],[o(3,2),y(3,2)],'color','c');
plot3([o(1,2),z(1,2)],[o(2,2),z(2,2)],[o(3,2),z(3,2)],'color','b');
end